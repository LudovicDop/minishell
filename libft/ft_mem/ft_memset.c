#include "../libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	void	*ret;

	i = n;
	ret = s;
	while (i > 0)
	{
		*(char *)s = c;
		i--;
		s++;
	}
	return (ret);
}
